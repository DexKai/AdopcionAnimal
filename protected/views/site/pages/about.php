  <dt>Misión</dt>
    <dd>SAA es una asociación sin fines de lucro que tiene como objetivo socorrer a la mayor cantidad de animales en situación de desamparo, maltrato yo/ abandono, albergándolos en nuestro refugio en condiciones dignas de vida, durante un tiempo establecido hasta que este pueda encontrar un hogar definido, responsable y sin carencias.</dd>
     <br>
  <dt>Visión</dt>
    <dd>Nuestra visión principal es que todos los animales desamparados dentro de nuestra organización puedan encontrar una buena vida al lado de una familia que los quiera, cuide y respete y a la vez poner fin a la cultura de crueldad y abandono contra animales de todas las especies, a través de diferentes métodos de concientización.</dd>
     <br>
  <dt>Objetivo general del Sistema</dt>
    <dd>Diseñar e implementar, un sistema web que permita: Facilitar la gestión de clientes, animales e historial de adopción de animales, tanto en la asignación de familias como en el seguimiento posterior a la adopción, para así aumentar la eficiencia y seguridad de la institución. Además ayudara a difundir la institución a través del internet aumentando progresivamente el número de futuros adoptantes.</dd>
     <br>
  <dt>Objetivos específicos</dt>
  	<dd><li type="square">Fomentar el buen trato hacia los diferentes tipos de animales.</li></dd>
  	<dd><li type="square">Asistir a los animales en situación de riesgo y rehabilitarlos.</li></dd>
  	<dd><li type="square">Dar un hogar a cada uno de los animales que se encuentran en las  dependencias de la organización</li></dd>
  	<dd><li type="square">Velar por la buena salud de los animales que ya fueron adoptados por un largo periodo de tiempo, para así, asegurarnos del buen trato que reciben</li></dd>
  	<dd><li type="square">Difundir la idea de protección y buen trato hacia los animales</li></dd>
  	<dd><li type="square">Denunciar actos de maltrato que tengan directa relación con la tenencia de animales.</li></dd>
